/*
Реализовать функцию sumPromises, которая принимает
в качестве аргументов промисы и возвращает сумму
результатов их выполнения.

Функция может принимать любое количество аргументов.
Можно использовать любые API процессы.
*/

const sumPromises = (...promises) => {
  // 1. Принимает любое количество промисов через ... (rest operator)
  //    ...promises превращает аргументы в массив, 
  //    те rest-оператор превращает переданные аргументы в массив
  //    Пример: sumPromises(p1, p2, p3) → promises = [p1, p2, p3]

  return (
    Promise.all(promises)
      // 2. Promise.all ждёт ВСЕ промисы и собирает их РЕЗУЛЬТАТЫ в массив
      //    [p1, p2, p3] → Promise.all → [результат1, результат2, результат3]

      .then(
        (results) =>
          // 3. then получает массив результатов
          //    results = [5, 10, 15]

          results.reduce((sum, num) => sum + num, 0),
        // 4. reduce складывает все числа
        //    sum = 0, num = 5 → 5
        //    sum = 5, num = 10 → 15
        //    sum = 15, num = 15 → 30
      )
  );
};

// Пример использования:
const promise1 = Promise.resolve(1);
const promise2 = Promise.resolve(2);
// Promise.resolve() создает уже выполненный промис с заданным значением

sumPromises(promise1, promise2).then(console.log); // 3
// then получает результат суммирования результатов промисов

/*
ШАГ 1: Есть промисы
       promise1 = 🤝 (внутри 1)
       promise2 = 🤝 (внутри 2)

ШАГ 2: Promise.all забирает ИХ РЕЗУЛЬТАТЫ
       🤝 + 🤝 → [1, 2]  ← массив чисел!

ШАГ 3: reduce складывает ЭТИ ЧИСЛА
       [1, 2] → 1 + 2 = 3

ШАГ 4: then получает ЭТУ СУММУ
       3 → console.log(3)
*/