/*
Даны две строки s и t, вернуть, если true является s подпоследовательностью,
t в false противном случае.

Подпоследовательность строки — это новая строка, которая образована из исходной
строки путем удаления некоторых (может быть ни одного) символов без нарушения
относительного положения оставшихся символов.
(т.е. "ace" является подпоследовательностью "abcde", а "aec" не является).

Мы проверяем, можно ли получить строку s из строки t,
вычеркивая лишние буквы из t, НО НЕ МЕНЯЯ ПОРЯДОК!
*/

const isSubsequence = function (s, t) {
  // 👇 sp - это указатель на строку s (subsequence)
  // Он показывает, сколько символов из s мы уже нашли в t
  let sp = 0;

  // 👇 tp - это указатель на строку t (text)
  // Он показывает, сколько символов в t мы уже проверили
  let tp = 0;

  // 👇 Идем, пока не закончилась ОДНА из строк
  // Если закончилась t - больше негде искать ❌
  // Если закончилась s - нашли все символы ✅
  while (sp < s.length && tp < t.length) {
    // 👇 Сравниваем текущие символы в обеих строках
    if (s[sp] === t[tp]) {
      // [] - мы обращаемся к символам строки по индексу!!!
      // === тк сравниваем значения, а не инексы 
      // ✅ СОВПАДЕНИЕ!
      // Двигаем указатель s - мы нашли очередной символ
      // Теперь будем искать следующий символ из s
      sp++;
    }
    // 👇 В ЛЮБОМ СЛУЧАЕ двигаем указатель t
    // Если было совпадение - идем дальше искать следующий символ
    // Если не было - пропускаем этот символ (он лишний)
    tp++;
  }

  // 👇 Если мы дошли до конца s (sp === s.length)
  // значит, нашли ВСЕ символы из s в t в правильном порядке
  // Если не дошли - значит, не хватило символов в t
  return sp === s.length;
  // Алгоритм вовращает true/false
};

/*
s - короткая строка
t - длинная строка

🎯 КЛЮЧЕВЫЕ МОМЕНТЫ:

sp = сколько символов из s мы уже нашли
tp = сколько символов в t мы уже проверили
sp двигается ТОЛЬКО при совпадении
tp двигается ВСЕГДА
В конце: sp === s.length = все символы найдены

✅ Время: O(n)
Мы проходим по t только ОДИН раз! Никаких вложенных циклов.

✅ Память: O(1)
Всего 2 переменные (sp, tp) — неважно, насколько длинные строки!

Указатели — это ИНДЕКСЫ (числа), которые хранят позиции в строках
*/

console.log(isSubsequence("abc", "ahbgdc")); // true
console.log(isSubsequence("abc", "ab")); // false (t короче)

